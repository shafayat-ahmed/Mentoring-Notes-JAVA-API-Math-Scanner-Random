public class MathAPI {
	public static void main(String[]args) {
		//tells the distance of the number from how much away from 0
		System.out.println("Absolute value ="+Math.abs(-2));
		//Square root
		System.out.println(Math.sqrt(4));
		//Ceil - round the number up
		System.out.println(Math.ceil(5.2));
		//Floor - round the number down
		System.out.println(Math.floor(5.2));
		//Max - Takes multiple parameter and determines the max of the numbers
		System.out.println(Math.max(5, 3));
		//Min - Determines the minimum number from multiple numbers
		System.out.println(Math.min(5, 3));
	}

}
